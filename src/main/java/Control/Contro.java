    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        // TODO add your handling code here:
        // cargo los datos de la tabla en los jtexfields
        try {
            int Selectfila = 0;
            Selectfila = tabla.getSelectedRow();
            if (Selectfila == -1) {
                showMessageDialog(null, "No a seleccionado ninguna fila a modificar", "Error", ERROR_MESSAGE);
            } else {
                DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();

                String id = (String) modelo.getValueAt(Selectfila, 0);

                String user = (String) modelo.getValueAt(Selectfila, 1);
                String pas = (String) modelo.getValueAt(Selectfila, 2);
                String edad = (String) modelo.getValueAt(Selectfila, 3);
                Object sexxo = modelo.getValueAt(Selectfila, 4);

                out.println("ID es ahora = " + id);
                txtID.setText(id);
                txtusuario.setText(user);
                txtpass.setText(pas);
                txtedad.setText(edad);
                seso.setSelectedItem(sexxo);

            }
        } catch (Exception e) {
            showMessageDialog(null, "Error " + e + " Intentelo de nuevo", "Error", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tablaMouseClicked

