    private void BotRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotRActionPerformed
        // TODO add your handling code here:
        ConsultaControl con = new ConsultaControl();

        U = RUser.getText();//usuario
        P = new String(RPass.getPassword());//password
        ed = parseInt(REdad.getText());//edad
        sex = sexo.getSelectedItem().toString();//tipo de sexo
        cambio = TipoU.getSelectedItem();//Tipo de usuario
        out.println("Cambio ahora es el numero : " + cambio);
        //Verifico si el usuario es admin o empleado
        if ((cambio.equals("Admin")) && (cambio != "Seleccionar:")) {
            tipu = 1;
        } else if ((cambio.equals("Empleado")) && (cambio != "Seleccionar:")) {
            tipu = 2;
        } else {
            showInternalMessageDialog(null, "Seleccione un tipo de usuario", "Error", ERROR_MESSAGE);
        }
        out.println("tipu es ahora = " + tipu);
        //Verifico si se llenaron todos los campos
        if ((U.isEmpty()) || (P.isEmpty()) || (ed == 0)) {
            showMessageDialog(null, "Debe rellenar todos los campos", "Error", ERROR_MESSAGE);
        } else {
            U = U.toLowerCase();
            P = P.toLowerCase();
            //Envio el nombre de usuario para verificar si existe en la BD
            boolean a = con.verificarUserBD(U);
            //Si existe pide un nombre distinto, si no registra el nuevo usuario
            if (a == true) {
                showMessageDialog(null, "El nombre de usuario ya existe en la BD por favor elija otro", "INFORMACION", INFORMATION_MESSAGE);
            } else {
                //paso parametros para insertar nuevo registro
                con.InsertarDatos(U, P, ed, sex, tipu);
            }
        }
    }//GEN-LAST:event_BotRActionPerformed

